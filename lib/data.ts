export interface WorkItem {
  id: string;
  code: string;
  name: string;
}

export interface SubCategory {
  id: string;
  code: string;
  name: string;
  items: WorkItem[];
}

export interface Category {
  id: string;
  code: string;
  name: string;
  icon: string;
  subCategories: SubCategory[];
}

export interface QuizItem {
  id: string;
  code: string;
  name: string;
  level: 2 | 3;
  subcatName?: string; // set for level-3 items
}

export function getSubCats(categoryId: string): SubCategory[] {
  return CATEGORIES.find((c) => c.id === categoryId)?.subCategories ?? [];
}

export function buildFlatItems(categoryId: string): QuizItem[] {
  const result: QuizItem[] = [];
  for (const sub of getSubCats(categoryId)) {
    result.push({ id: sub.id, code: sub.code, name: sub.name, level: 2 });
    for (const wi of sub.items) {
      result.push({ id: wi.id, code: wi.code, name: wi.name, level: 3, subcatName: sub.name });
    }
  }
  return result;
}

